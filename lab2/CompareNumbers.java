public class CompareNumbers {
    public static void main(String[] args){
        int val1 = 2;
        int val2 = 2;

        if (val1 < val2) {
            System.out.println(val1 + " < " + val2);
        }else if (val1 == val2) {
            System.out.println(val1 + " = " + val2);
        }else {
            System.out.println(val1 + " > " + val2);
        }
    }
}
